class Exemplo {
    constructor(animal, nome) {
        this.animal = null;
        this.nome = null;
        this.animal = animal;
        this.nome = nome;
    }
}
let bicho = new Exemplo("Collie", "Simba");
console.log(`Espécie: ${bicho.animal}`);
console.log(`Nome: ${bicho.nome}`);


